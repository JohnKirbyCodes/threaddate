// Read the file
const filePath = path.join(__dirname, 'brand-data.ts');
let content = fs.readFileSync(filePath, 'utf8');

// Process each brand
Object.entries(brandCountries).forEach(([brandName, countryCode]) => {
  // Skip already processed brands (those with country_code already)
  if (brandName === "Nike" || brandName === "Adidas" || brandName === "Champion" ||
      brandName === "New Balance" || brandName === "Reebok" || brandName === "Puma" ||
      brandName === "Asics" || brandName === "Converse" || brandName === "Vans" ||
      brandName === "Fila" || brandName === "Umbro") {
    return;
  }

  // Find the brand entry and add country_code after founded_year
  const brandRegex = new RegExp(
    `(\\{ name: "${brandName.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}", [^}]*founded_year: \\d+)(, description:)`,
    'g'
  );

  content = content.replace(brandRegex, `$1, country_code: "${countryCode}"$2`);
});

// Write the file back
fs.writeFileSync(filePath, content, 'utf8');

